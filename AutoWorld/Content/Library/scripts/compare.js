		myApp.controller("myController", function($scope){
			$scope.hideTitle1 = true;//hide title
			$scope.hideTitle2 = true;
            //Checkbox Selected
            var mol = window.location.search.substring(1);
            $('#'+ mol).prop('checked', true);  
            //Ready function
			angular.element(document).ready(function(){
				$('input[type=checkbox]').on('change', function (e) {
				    if ($('input[type=checkbox]:checked').length > 3) {
				        $(this).prop('checked', false);
				    }
				});
                // $('input[type=checkbox]')=window.location.search.substring(1);
				//function when click checkbox
				$('input[type=checkbox]').on('change',function() {
				$scope.options = options;
				$scope.motos = motos;
				var val=[];
				$('input:checkbox:checked').each(function(i){
					val[i] = $(this).val();
				});
				$scope.idsToTake = val;
				//function when click button "Compare"
				$scope.myFunc = function(){
					//remove information unchecked
					$('input:checkbox').parents(".p-check").hide();
					$('input:checkbox:checked').parents(".p-check").show();
					//create fitter
					$scope.filterUserIds = function(i){
					return($scope.idsToTake.indexOf(i.id) != -1);

					};
					//setup style for the button
					$scope.style1 = {'background-color': '#ffffff'};
					$scope.style2 = {'background-color': '#f99c53'};
					$scope.idsMoto =['xe16','xe17','xe18','xe19','xe20','xe21','xe22','xe23','xe24','xe25'];
					$.each($scope.idsToTake,function(key,value){
						if($scope.idsMoto.indexOf(value)!= -1)
							{
								$scope.hideTitle2 = false;
							}
						else{$scope.hideTitle1 = false;}	
					});
						
				};
				//function when click "Make choice"
				$scope.back = function(){
					$('input:checkbox').parents(".p-check").show();
					$scope.filterUserIds = "+";
					$scope.hideTitle1 = true;
					$scope.hideTitle2 = true;
					$scope.style1 = {'background-color': '#f99c53'};
					$scope.style2 = {'background-color': '#ffffff'};
				};
			});	
		});			
	});